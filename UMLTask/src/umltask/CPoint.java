package umltask;

/**
 *
 * @author Alex
 */
public class CPoint {
    
    private double dX;
    private double dY;

    public CPoint(double dX, double dY) {
        this.dX = dX;
        this.dY = dY;
    }

    public double getdX() {
        return dX;
    }

    public void setdX(double dX) {
        this.dX = dX;
    }

    public double getdY() {
        return dY;
    }

    public void setdY(double dY) {
        this.dY = dY;
    }
}
